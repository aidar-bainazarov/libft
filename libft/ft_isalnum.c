#include "libft.h"

int	ft_isalnum(int	c)
{
	if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z'))
		return (c);
	if (c >= 'A' && c <= 'Z')
		return (c);
	return (0);
}
